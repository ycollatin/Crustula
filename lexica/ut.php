QVAM VT
nec habui quidquam antiquius, quam ut Pansam statim convenirem,
quem cum obiurgarem, quod parum valetudini parceret, tum ille, nihil sibi longius fuisse, quam ut me videret.

COMME
ut mones, frenum momordi,
Sed, ut spero, propediem te videbo.
illud dicam, ut sentio.
illud doleo, in ista loca venire me, ut constitueram, non potuisse;
Sed iocabimur alias coram, ut spero, brevi tempore.
Cratippo me scito non ut discipulum, sed ut filium esse coniunctissimum;
Quod reliquum est, sustenta te, mea Terentia, ut potes.
Ceteris iniuriis, quae propositae sunt a Catone, facile, ut spero, resistemus.
Hoc mihi ut testi velim credas;
Pomponium Atticum sic amo, ut alterum fratrem;

COMPLETIVE SIMPLE
Huius me constantiae puto fore ut numquam paeniteat.
in animo habere debemus, homines nos ut esse meminerimus,
Sed ita fato nescio quo contigisse arbitror, ut tibi ad me ornandum semper
   detur facultas, mihi ad te remunerandum nihil suppetat praeter voluntatem: 
potest fieri, ut fallar.
Non putavi fieri posse, ut mihi verba deessent, sed tamen in M. Laenio commendando deerunt; 
itaque optamus, ut quam primum te in Italia videamus:
homo amens et perditus, nullam aliam ob causam me auctorem fuisse Caesaris interficiendi
  criminatur, nisi ut in me veterani incitentur.

SIC, ITA, TAM... VT
velim, ut meremur de re publica, sic felices simus.
Tuae res gestae ita notae sunt ut trans montem Taurum etiam de Matrinio sit auditum.
statim ita sum levatus, ut mihi deus aliquis medicinam fecisse videatur
ut enim Aristarchus Homeri versum negat, quem non probat, sic tu?
ita sim praesto, ut me non solum omnia debere tua causa, que possim, sed etiam quae non possim, putem.
nec sum tam stultus, ut te usura falsi gaudii frui velim.
ut illud erat molestum, sic hoc est iucundum
his tuis factis sic incensus sum, ut nihil umquam in amore fuerit ardentius;
sic fit, ut, si quid praeterea de me audiat, non audiendum putet.
cum aut scribo ad vos aut vestras lego, conficior lacrimis sic, ut ferre non possim.
adeo est enim invisus mihi, ut nihil non acerbum putem, quod commune cum illo sit;
Primo ita pugnatum est, ut acrius non posset ex utraque parte pugnari;

AUTRES CORRELATIONS
Res sunt eiusmodi, ut, si Romae sis, nihil te praeter tuos delectare possit;
Homines sumus, ea lege natos, ut omnibus telis fortunae proposita sit vita nostra
tantis pedum doloribus afficitur, ut se conveniri nolit.
hoc usque eo suave est, ut, si acciderit, tota vita risus nobis deesse non possit.
Pisonis humanitas, virtus, amor in omnes nos tantus est, ut nihil supra possit
hoc animo scito omnes esse sanos, ut mortem servituti anteponant.

