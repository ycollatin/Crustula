V. DE DEMANDE
Rogas, ut mea tibi scripta mittam
Qua re ut ad me omnia quam diligentissime perscribas te vehementer rogo.
Petis a me ut Bibulo te quam diligentissime commendem.
a me petivit, ut Laodiceam protinus irem;
quod ut facias, vehementer te rogo.
postulat enim hoc Hirtius ut faciam.

FACERE ET AUTRES VERBES ET EXPRESSIONS D'EFFORT
fac, ut quam primum venias
ipse operam dabo te ut quam primum videam.
Ego, ut me tibi amicissimum esse et aequi et iniqui intelligant, curabo
faciendum mihi putavi, ut tuis litteris brevi responderem.
cura igitur, ut quam primum venias;
Ab Atheniensibus, locum sepulturae intra urbem ut darent, impetrare non potui.
Immo vero, si me amas, tu fac, ut sciam, quid de nobis futurum sit;
non mediocris adhibenda mihi est cura, ut rei publicae me civem dignum tuis laudibus praestem
Tu velim, ut consuesti, nos absentes diligas et defendas.  
Da operam, ut valeas: hoc mihi gratius facere nihil potes.

VT FINAL
Haec ad te in praesentia scripsi ut sperares te adsequi id quod optasses.
tu velim scribas ad me, quid agas et ubi futurus sis, ut aut quo scribam aut quo veniam scire possim.
constitui ad te venire, ut et viderem te et viserem et coenarem etiam;
velim tabellarios instituatis certos, ut quotidie aliquas a vobis litteras accipiam;
Fac valeas et ad me tabellarios mittas, ut sciam, quid agatur et vos quid agatis.

EXPRESSIONS
